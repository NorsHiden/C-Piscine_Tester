void	ft_rev_int_tab(int *tab, int size)
{
	int	c;
	int	s;

	c = 0;
	while (c < size)
	{
		s = tab[c];
		tab[c] = tab[size - 1];
		tab[size - 1] = s;
		c++;
		size--;
	}
}
