void	ft_sort_int_tab(int *tab, int size)
{
	int	c;
	int	s;
	int	i;

	i = 0;
	while (i < size)
	{
		c = 0;
		while (c < size)
		{
			if (tab[c] > tab[c + 1])
			{
				s = tab[c];
				tab[c] = tab[c + 1];
				tab[c + 1] = s;
			}
			c++;
		}
		i++;
	}
}
